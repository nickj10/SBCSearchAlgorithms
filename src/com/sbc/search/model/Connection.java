package com.sbc.search.model;

public class Connection{
    private String from;
    private String to;
    private long distance;
    private long duration;

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }

    public long getDistance() {
        return distance;
    }

    public long getDuration() {
        return duration;
    }
}
